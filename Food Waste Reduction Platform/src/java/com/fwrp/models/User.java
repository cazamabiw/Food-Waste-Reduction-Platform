package com.fwrp.models;
import java.util.Date;
import java.util.Date;
/**
 * This class defines a User entity containing multiple attributes including
 * userId, firstName, lastName, email, password, addressLine, city, province,
 * postalCode, notified, and lastUpdated.
 *
 * @author Sreelakshmi Odatt Venu
 */
public class User {

    private int userId;
    private String firstName;
    private String lastName;
    private String email;
    private String password;
    private String addressLine;
    private String city;
    private String province;
    private String postalCode;
    private boolean notified;
    private Date lastUpdated;

    // Getters and Setters
    public int getUserId() {
        return userId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getAddressLine() {
        return addressLine;
    }

    public void setAddressLine(String addressLine) {
        this.addressLine = addressLine;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getProvince() {
        return province;
    }

    public void setProvince(String province) {
        this.province = province;
    }

    public String getPostalCode() {
        return postalCode;
    }

    public void setPostalCode(String postalCode) {
        this.postalCode = postalCode;
    }

    public boolean isNotified() {
        return notified;
    }

    public void setIsNotified(boolean notified) {
        this.notified = notified;
    }

    public Date getLastUpdated() {
        return lastUpdated;
    }

    public void setLastUpdated(Date lastUpdated) {
        this.lastUpdated = lastUpdated;
    }
}
